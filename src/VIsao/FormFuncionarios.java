    //método para tabela, preencher tabela.
    public void preencherTabela(String Sql)
    {
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID","Nome","Cargo","Idade"};
        conexao.conexao();
        conexao.executaSql(Sql);
        try{
            conexao.rs.first();
            // enquanto tiver dados ele apresenta
            do{
                dados.add(new Object[]{conexao.rs.getInt("id_funcionarios"),conexao.rs.getString("NOME_FUNCIONARIOS"),conexao.rs.getString("CARGO_FUNCIONARIOS"),conexao.rs.getInt("idade_funcionarios")});
                
            }while(conexao.rs.next());
        }catch(SQLException ex){
                    JOptionPane.showMessageDialog(rootPane, "Erro no ArrayList"+ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados,colunas);

        jTableFuncionarios.setModel(modelo);
        jTableFuncionarios.getColumnModel().getColumn(0).setPreferredWidth(35);
        //aumentar tamanho da coluna 
        jTableFuncionarios.getColumnModel().getColumn(0).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(220);
        jTableFuncionarios.getColumnModel().getColumn(1).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableFuncionarios.getColumnModel().getColumn(2).setResizable(false);
        jTableFuncionarios.getColumnModel().getColumn(1).setPreferredWidth(120);
        jTableFuncionarios.getColumnModel().getColumn(1).setResizable(false);
        jTableFuncionarios.getTableHeader().setReorderingAllowed(false);
        // TABELA NÃO PODE SER DIMENSIONADA
        jTableFuncionarios.setAutoResizeMode(jTableFuncionarios.AUTO_RESIZE_OFF);
        // só selecionar um dado por vêz
        jTableFuncionarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conexao.desconecta();
        
        
        
    }
    
