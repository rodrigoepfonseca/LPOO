    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
       if(flag ==1 )
       {
        modelo.setNome(jTextFieldNome.getText());
        // modelo diferente porque faz consulta em banco
        modelo.setCargo((String)jComboBoxCargo.getSelectedItem());
        // converter de inteiro para String
        modelo.setIdade(Integer.parseInt(jFormattedTextFieldIdade.getText()));
        Controle.Salvar(modelo);      
        
        // após salar ele deve limpar :)
        jTextFieldNome.setText("");
        jFormattedTextFieldIdade.setText("");
        jTextFieldNome.setEnabled(false);
        jFormattedTextFieldIdade.setEnabled(false);
        jComboBoxCargo.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonCancelar.setEnabled(false);
       }else
        {
            modelo.setCodigo(Integer.parseInt(jTextFieldID.getText()));
            modelo.setNome(jTextFieldNome.getText());
            modelo.setCargo((String)jComboBoxCargo.getSelectedItem());
            modelo.setIdade(Integer.parseInt(jFormattedTextFieldIdade.getText()));
            Controle.Editar(modelo);
            // limpar campos
            jTextFieldID.setText("");
            jTextFieldNome.setText("");
            jFormattedTextFieldIdade.setText("");
            // liberando botões
            jTextFieldNome.setEnabled(false);
            jFormattedTextFieldIdade.setEnabled(false);
            jComboBoxCargo.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
        }
        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

